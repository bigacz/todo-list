class Note {
    constructor(title, description, dueDate, creationDate, notes, state, priority, project) {
        this.title = title; 
        this.description = description; 
        this.dueDate = dueDate;
        this.creationDate = creationDate; 
        this.notes = notes; 
        this.state = state; 
        this.priority = priority;
        this.project = project;
    }
}

